public class MathPackage {
    public static void main(String args[]){
        int a = 5;
        System.out.println("Math.sqrt(a) = " + Math.sqrt(a));
        System.out.println("Math.max(a, 2) = " + Math.max(a, 2));
        System.out.println("Math.min(a, 2) = " + Math.min(a, 2));
        System.out.println("Math.pow(a, 2) = " + Math.pow(a, 2));
        System.out.println("Math.abs(a) = " + Math.abs(a));
    }
}
